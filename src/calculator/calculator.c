/*
Takes two integers as intput and returns their sum
*/
int add(int number1, int number2) {
    int sum = number1 + number2;
    return sum;
}

/*
Takes two integers as intput and returns their difference
*/
int subtract(int number1, int number2) {
    int difference = number1 - number2;
    return difference;
}

/*
Takes two integers as intput and returns their product
*/
int multiply(int number1, int number2) {
    int product = number1 * number2;
    return product;
}

/*
Takes two integers as intput and returns their quotient
*/
int divide(int number1, int number2) {
    int quotient = number1 / number2;
    return quotient;
}
